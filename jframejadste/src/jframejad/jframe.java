    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        String serie = null;
        String curso = null;
        if (jRadioButton3.isSelected()) {
            serie = jRadioButton3.getText();
        } else if (jRadioButton4.isSelected()) {
            serie = jRadioButton4.getText();
        } else if (jRadioButton5.isSelected()) {
            serie = jRadioButton5.getText();
        }
        
        if (jRadioButton1.isSelected()) {
            curso = jRadioButton1.getText();
        } else{
            curso = jRadioButton2.getText();
        }
        
        if((jTextField2.getText().isEmpty())){
        JOptionPane.showMessageDialog(null, "Nome: " + jTextField1.getText() + "\nData de Nascimento: " + jFormattedTextField1.getText() + "\nSérie: " + serie + "\nCurso: " + curso);
        }else{
            JOptionPane.showMessageDialog(null, "Nome: " + jTextField1.getText() + "\nData de Nascimento: " + jFormattedTextField1.getText() + "\nSérie: " + serie + "\nCurso: " + curso + "\nNome do Responsável: " + jTextField2.getText());
        }
        
        jTextField1.setText("");
        jFormattedTextField1.setText("");
        jTextField2.setText("");
        jLabel8.setVisible(false);
        jTextField2.setVisible(false);
    }//GEN-LAST:event_jButton2MouseClicked

