    private void jFormattedTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jFormattedTextField1KeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            Date dataNascimento = null;
            SimpleDateFormat formatoData = new SimpleDateFormat("dd/MM/yyyy");
            formatoData.setLenient(false);
            String datadenas = (jFormattedTextField1.getText());
            try {
                dataNascimento = formatoData.parse(datadenas);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data Inválida!");
                jFormattedTextField1.setText("");
            }

            Calendar hoje = Calendar.getInstance();
            hoje.getTime();

            Calendar dataNasc = Calendar.getInstance();
            dataNasc.setTime(dataNascimento);

            int idade = hoje.get(Calendar.YEAR) - dataNasc.get(Calendar.YEAR);

            if (hoje.get(Calendar.MONTH) < dataNasc.get(Calendar.MONTH)) {
                idade--;
            }

            if (idade < 18) {
                jLabel8.setVisible(true);
                jTextField2.setVisible(true);
                jRadioButton3.requestFocus();
                jRadioButton3.setSelected(true);
                jRadioButton1.setSelected(true);
            } else {
                jLabel8.setVisible(false);
                jTextField2.setVisible(false);
                jRadioButton3.requestFocus();
                jRadioButton3.setSelected(true);
                jRadioButton1.setSelected(true);
            }
        }
    }//GEN-LAST:event_jFormattedTextField1KeyPressed

